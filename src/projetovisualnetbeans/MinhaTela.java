    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Pessoa pessoa = pessoaSelecionada == null ? new Pessoa() : pessoaSelecionada;
        pessoa.setNome(tfNome.getText());
        pessoa.setSobrenome(tfSobrenome.getText());
        if (rbFeminino.isSelected()) {
            pessoa.setSexo("F");
        } else if (rbMasculino.isSelected()) {
            pessoa.setSexo("M");
        } else {
            pessoa.setSexo(null);
        }
        pessoa.setAtivo(cbAtivo.isSelected());
        //
        if (pessoaSelecionada == null) {
            listaPessoas.add(pessoa);
        }
        //
        JOptionPane.showMessageDialog(null, "Pessoa cadastrada com sucesso", "Cadastro de Pessoas", JOptionPane.INFORMATION_MESSAGE);
        reiniciaCadastro();
        atualizaLista();
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        listaPessoas.remove(pessoaSelecionada);
        atualizaLista();
        reiniciaCadastro();
    }//GEN-LAST:event_btExcluirActionPerformed

