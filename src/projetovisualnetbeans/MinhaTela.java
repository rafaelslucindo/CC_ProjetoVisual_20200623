    private void btAnexaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAnexaActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int returnVal = fileChooser.showOpenDialog(null);
        //
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            JOptionPane.showMessageDialog(null, file.getPath(), "Seleção Arquivo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "NADA", "Seleção Arquivo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btAnexaActionPerformed

    private void reiniciaCadastro() {
        tfNome.setText("");
        tfSobrenome.setText("");
        bgSexo.clearSelection();
        cbAtivo.setSelected(false);
        pessoaSelecionada = null;
        //
        tfNome.requestFocus();
        //
        configuraTela();
    }

    private void atualizaLista() {
        DefaultListModel listModel = new DefaultListModel();
        for (Pessoa pessoa : listaPessoas) {
            listModel.addElement(pessoa);
        }
        lPessoas.setModel(listModel);
        //
        salvaListaArquivo();
    }

    private void salvaListaArquivo() {
        try {
            FileOutputStream fout = new FileOutputStream(new File("R:\\cadastro"));
            ObjectOutputStream oos;
            oos = new ObjectOutputStream(fout);
            oos.writeObject(listaPessoas);
        } catch (Exception ex) {
            ex.toString();
        }

    }

    private void carregaArquivo() {
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream(new File("R:\\cadastro")));
            listaPessoas = (ArrayList<Pessoa>) ois.readObject();
        } catch (Exception ex) {
            ex.toString();
        }
    }

    private void iniciaEventos() {
        lPessoas.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                selecionaPessoa();
            }
        });
    }

    private void selecionaPessoa() {
        if (lPessoas.getSelectedIndex() != -1) {
            pessoaSelecionada = listaPessoas.get(lPessoas.getSelectedIndex());
            tfNome.setText(pessoaSelecionada.getNome());
            tfSobrenome.setText(pessoaSelecionada.getSobrenome());
            cbAtivo.setSelected(pessoaSelecionada.getAtivo());
            if (pessoaSelecionada.getSexo() == null) {
                bgSexo.clearSelection();
            } else if (pessoaSelecionada.getSexo().equals("M")) {
                rbMasculino.setSelected(true);
            } else {
                rbFeminino.setSelected(true);
            }
            configuraTela();
        } else {
            reiniciaCadastro();
        }
    }

